public class Loops {
    public static void main(String[] args) {

        System.out.println("Print Hello 5 Times");

        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello");
        }

        System.out.println();

        System.out.println("Print Numbers 1 to 5");

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println();

        System.out.println("Print Numbers 10 to 15");

        for (int i = 10; i <= 15; i++) {
            System.out.println(i);
        }
    }
}