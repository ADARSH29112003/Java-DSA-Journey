// Creates - CharAtDemo
// String Language = "Java";

// Print:
// First Character = J
// last Character = a

public class CharAtDemo {
    public static void main(String[] args) {
        String language = "Java";

        System.out.println("First Character : " + language.charAt(0));
        System.out.println("Last Character : " + language.charAt(3));
    }
}
